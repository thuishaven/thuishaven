  "https://github.com/thuishaven/thuishaven/blob/main/patterns";

/** Error thrown by tools for invalid input (unknown id, unknown target, ...). */
export class ToolError extends Error {
  constructor(message: string) {
    super(message);
    this.name = "ToolError";
  }
}
